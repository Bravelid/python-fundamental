"""
Simple loop program with while
"""
# example

jumlahbuku = 1000
print("ibu berkata 'Baca Semua bukumu'")
jumlahbukudibaca = 0
print(f"buku yang saya baca adalah {jumlahbukudibaca}")

while jumlahbukudibaca < jumlahbuku:
    jumlahbukudibaca = jumlahbukudibaca + 1
    print(f"jumlah buku yang saya baca {jumlahbukudibaca} ")

print(f"bu saya sudah membaca {jumlahbukudibaca} buku")
